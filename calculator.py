def summ(a,b):
    " adding two numbers a and b"
    summ=a+b
    return summ
def sub(a,b):
    "subtracting two numbers a and b"
    sub=a-b
    return sub
def mul(a,b):
    "multiplying two numbers a and b"
    mul=a*b
    return mul
def div(a,b):
    "dividing two numbers a and b"
    div=a/b
    return div

    
    
    
        
      
    



